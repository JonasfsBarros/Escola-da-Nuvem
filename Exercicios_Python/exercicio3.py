#3- Calculadora de Volume
#* Crie um programa que calcula o volume de uma caixa retangular. Use as seguintes dimensões:

#* Comprimento: 12 cm
# * Largura: 14 cm
#* Altura: 20 cm
#O programa deve calcular o volume e exibir o resultado em cm³.

# Dimensões da caixa retangular
comprimento = 12  # em cm   
largura = 14      # em cm   
altura = 20       # em cm
# Cálculo do volume
volume = comprimento * largura * altura 
# Exibição do resultado
print(f"O volume da caixa retangular é: {volume} cm³")


